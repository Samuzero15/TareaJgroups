    private void inicializaTodo(){
        initComponents();
        panelPizarra1.setPP(this);
        
        lblTamano.setText("" + sldTamano.getValue());
        ArrayList<String> nombreColores = new ArrayList<>();
        HashMap<String, Color> listaColores = panelPizarra1.getListaColoresSelect();
        for(String a : listaColores.keySet()){
            //System.out.println("Add " + a);
            nombreColores.add(a);
        }
        panelPizarra1.setColor(listaColores.get(nombreColores.get(0))); // Inicia con el color preseleccionado
        panelPizarra1.setTamano(sldTamano.getValue());
        comColor.setModel(new DefaultComboBoxModel<>(nombreColores.toArray(new String[0])));
        
    }
    
