    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        
        if(cr != null && cr.getModoUsuario() == ModoUsuario.PROFESOR){
            cr.cierraPizarras();
        }else {
            this.dispose();
            System.exit(0);
        }
    }//GEN-LAST:event_formWindowClosing

    public void sendStringDibujo(String puntosAString) {
        try {
            //System.out.println("Envio dibujo");
            Paquete p = new Paquete(TipoMensaje.DIBUJO, puntosAString);
            this.cr.getCanal().send(new Message(null, null, p));
            this.strDibujo = puntosAString;
        } catch (Exception ex) {
            //ex.printStackTrace();
            System.out.println("He fallado en retransmitir el dibujo");
        }
    }

    
