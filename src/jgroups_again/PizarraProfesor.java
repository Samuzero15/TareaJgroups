    private void comColorItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comColorItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
          String item = (String) evt.getItem();
            // do something with object
            //System.out.println(item);
          HashMap<String, Color> listaColores = panelPizarra1.getListaColoresSelect();
          for(String col : listaColores.keySet()){
              if(item.equals(col)) panelPizarra1.setColor(listaColores.get(col));
          }
       }
    }//GEN-LAST:event_comColorItemStateChanged

